package com.exemplo.models;

public enum TipoUsuario {
    TREINADOR("Treinador"),
    CLIENTE("Cliente");

    private final String descricao;

    TipoUsuario(String descricao) {
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }

    @Override
    public String toString() {
        return descricao;
    }
}